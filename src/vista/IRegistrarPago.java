    private void mostrarInformaciondelRecibo(){
        Date fecha = new Date();
        SimpleDateFormat formateador = new SimpleDateFormat("dd/MM/yyyy"); 
        String f = formateador.format(fecha);
        this.jtNoRecibo.setText(""+vr.getId_recibo());
        this.jtfZona.setText(vr.getZona());
        this.jtfUsuario.setText(vr.nombreCompletodeUsusrio());
        this.jtfDireccion.setText(vr.direcciondeUsusrio());
        this.jtfMes.setText(mes(vr.getMes()));
        this.jtfActual.setText(""+vr.getMontoDouble());
        this.jtfNDI.setText(""+vr.getNdi());
        this.jtfFecha.setText(f);
    }
    
    private String mes(int m){
        String mes = "";
        switch(m){
            case 1: 
                mes = "ENERO";
            break;
            case 2: 
                mes = "FEBRERO";
            break;
            case 3: 
                mes = "MARZO";
            break;
            case 4: 
                mes = "ABRIL";
            break;
            case 5: 
                mes = "MAYO";
            break;
            case 6: 
                mes = "JUNIO";
            break;
            case 7: 
                mes = "JULIO";
            break;
            case 8: 
                mes = "AGOSTO";
            break;
            case 9: 
                mes = "SEPTIEMBRE";
            break;
            case 10: 
                mes = "OCTUBRE";
            break;
            case 11: 
                mes = "NOVIEMBRE";
            break;
            case 12: 
                mes = "DICIEMBRE";
            break;
            default: 
                mes = "ENERO";
            break;
        }
        return mes;
    }

